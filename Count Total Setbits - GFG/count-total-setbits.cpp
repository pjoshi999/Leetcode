class Solution {
  public:
    long long countBits(long long N) {
        // code here
        long long int res = 0;
        for(long long int i=1; i<=N; i++) {
            long long int count1s = __builtin_popcountll(i);
            res += count1s;
        }
        return res;
    }
};


